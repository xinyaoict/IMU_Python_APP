package com.llui.idrink.Enums;
/**
 * MeasurementStatus defines the possible states of a measurement.
 * - NOT_STARTED: Indicates that the measurement has not been started yet.
 * - COMPLETED: Indicates that the measurement has been successfully completed.
 * - PARTIAL_COMPLETED: Indicates that the measurement was partially completed.
 * - FAILED: Indicates that the measurement failed to complete.
 */
public enum MeasurementStatus {
    NOT_STARTED,
    COMPLETED,
    PARTIAL_COMPLETED,
    FAILED
}
